- (NSString*)chatStringRepresentation {
    NSString* dateString = nil;
    if ([self isToday]) {
        dateString = @"Today";
    } else if ([self isYesterday]) {
        dateString = @"Yesterday";
    } else {
        if ([self isThisWeek] || [NSDate date].weekday < 4) {
            dateString = [self stringFromDateWithDateFormat:@"EEEE"];
        } else {
            dateString = [self stringRepresentation];
        }
    }
    return dateString;
}

- (NSString*)timeStringRepresentation {
    return [self stringFromDateWithDateFormat:@"hh:mm a"];
}

- (NSString*)dayStringRepresentation {
    NSString* dateString = nil;
    if ([self isToday]) {
        dateString = @"Today";
    } else if ([self isYesterday]) {
        dateString = @"Yesterday";
    } else {
        NSString *format = @"yyyy-MM-dd";
        if ([self isThisYear])
            format = @"MMM dd";
        else
            format = @"MMM dd, yyyy";
        
        dateString = [self stringFromDateWithDateFormat:format];
    }
    return dateString;
}

- (NSString*)postDetailStringRepresentation {
   NSString *dateString = nil;
   NSDate *now = [NSDate date];
   
   if ([self isToday]) {
      NSInteger seconds   = [now timeIntervalSinceDate:self];
      NSInteger minutes   = [self minutesBeforeDate:now];
      NSInteger hours     = [self hoursBeforeDate:now];
      
      if (seconds < 60) {
         dateString = [NSString stringWithFormat:@"%d %@ %@", (int)seconds, seconds > 1 ? @"seconds" : @"second", @"ago"];
      }
      else if (minutes < 60) {
         dateString = [NSString stringWithFormat:@"%d %@ %@", (int)minutes, minutes > 1 ? @"minutes" : @"minute", @"ago"];
      } else {
         if (hours < 24) {
            dateString = [NSString stringWithFormat:@"%d %@ %@", (int)hours, hours > 1 ? @"hours" : @"hour", @"ago"];
         }
      }
   } else if ([self isYesterday]) {
      dateString = [NSString stringWithFormat:@"%@ %@ %@", @"Yesterday", @"at", [self stringFromDateWithDateFormat:@"hh:mm a"]];
   } else if ([self isThisYear]) {
      dateString = [NSString stringWithFormat:@"%@ %@ %@", [self stringFromDateWithDateFormat:@"MMM dd"], @"at", [self stringFromDateWithDateFormat:@"hh:mm a"]];
   } else {
      dateString = [NSString stringWithFormat:@"%@", [self stringFromDateWithDateFormat:@"MMM dd, yyyy"]];
   }
   
   return dateString;
}

- (NSString*)activityStringRepresentation {
   NSString *dateString = nil;
   NSDate *now = [NSDate date];
   
   if ([self isToday]) {
      dateString = [self stringFromDateWithDateFormat:@"h:mm a"];
   } else if ([self isYesterday]) {
      dateString = [NSString stringWithFormat:@"%d%@", (int)[self hoursBeforeDate:now], @"h"];
   }
   else {
      dateString = [NSString stringWithFormat:@"%d%@", (int)[self daysBeforeDate:now], @"d"];
   }
   
   return dateString;
}

- (NSString*)stringFromDateWithDateFormat:(NSString*)dateFormat {
    static NSDateFormatter* sDateFormatter = nil;
    if (!sDateFormatter)
        sDateFormatter = [[NSDateFormatter alloc]init];

    [sDateFormatter setDateFormat:dateFormat];
    return [sDateFormatter stringFromDate:self];
}

